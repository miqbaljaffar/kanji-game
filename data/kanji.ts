export function getRandomOptions(correct: KanjiEntry, all: KanjiEntry[], count: number = 4): KanjiEntry[] {
  const pool = all.filter((k) => k.id !== correct.id);
  const shuffled = [...pool].sort(() => Math.random() - 0.5);
  const wrong = shuffled.slice(0, count - 1);
  const options = [...wrong, correct].sort(() => Math.random() - 0.5);
  return options;
}

export function shuffleArray<T>(arr: T[]): T[] {
  return [...arr].sort(() => Math.random() - 0.5);
}
